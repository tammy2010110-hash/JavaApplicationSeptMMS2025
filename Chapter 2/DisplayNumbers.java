import java.util.Scanner;
public class DisplayNumbers {
    public static void main(String[] args) {
        
        // Using println
        System.out.println("1 2 3 4");

        // Using print
        System.out.print("1 ");
        System.out.print("2 ");
        System.out.print("3 ");
        System.out.print("4\n");

        // Using printf
        System.out.printf("%d %d %d %d%n", 1, 2, 3, 4);
    }
}
